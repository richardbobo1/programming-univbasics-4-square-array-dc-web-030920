def square_array(numbers)
   counter = 0
 while counter < numbers.length do
   puts numbers[counter]**numbers[counter]
   counter += 1
 end 
end

def square_array(new_numbers)
   counter = 0
 while counter < numbers.length do
   puts new_numbers[counter]**new_numbers[counter]
   counter += 1
 end 
end